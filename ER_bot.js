const mobileMenuOverlay = document.getElementById('mobileMenuOverlay');
const mobileNavLinks = document.querySelectorAll('.mobile-nav-link');
const modal = document.getElementById('modal');
const modalClose = document.getElementById('modalClose');
const moduleCards = document.querySelectorAll('.module-card');

// Mobile Menu
function toggleMobileMenu() {
    mobileMenu.classList.toggle('active');
    document.body.style.overflow = mobileMenu.classList.contains('active') ? 'hidden' : '';
}

function closeMobileMenu() {
    mobileMenu.classList.remove('active');
    document.body.style.overflow = '';
}

mobileMenuBtn.addEventListener('click', toggleMobileMenu);
mobileMenuOverlay.addEventListener('click', closeMobileMenu);

mobileNavLinks.forEach(link => {
    link.addEventListener('click', (e) => {
        closeMobileMenu();
        const href = link.getAttribute('href');
        if (href && href.startsWith('#')) {
            e.preventDefault();
            smoothScroll(href);
        }
    });
});

// Smooth Scroll
function smoothScroll(target) {
    const element = document.querySelector(target);
    if (element) {
        const headerHeight = document.querySelector('.header').offsetHeight;
        const targetPosition = element.offsetTop - headerHeight - 20;
        window.scrollTo({
            top: targetPosition,
            behavior: 'smooth'
        });
    }
}

// All anchor links
document.querySelectorAll('a[href^="#"]').forEach(link => {
    link.addEventListener('click', (e) => {
        const href = link.getAttribute('href');
        if (href && href !== '#') {
            e.preventDefault();
            smoothScroll(href);
        }
    });
});

// Modal
function openModal(moduleId) {
    const data = moduleData[moduleId];
    if (!data) return;

    document.getElementById('modalTitle').textContent = data.title;
    document.getElementById('modalSubtitle').textContent = data.subtitle;

    // Steps
    const stepsContainer = document.getElementById('modalSteps');
    stepsContainer.innerHTML = data.steps.map(step => `
        <li>
            <div class="step-number">${step.number}</div>
            <div class="step-content">
                <h4>${step.title}</h4>
                <p>${step.description}</p>
            </div>
        </li>
    `).join('');

    // Sidebar
    document.getElementById('sidebarTip').innerHTML = 
        `<strong><i class="fas fa-lightbulb"></i> –°–æ–≤–µ—Ç:</strong> ${data.tip}`;
    document.getElementById('sidebarWarning').innerHTML = 
        `<strong><i class="fas fa-exclamation-triangle"></i> –í–∞–∂–Ω–æ:</strong> ${data.warning}`;
    document.getElementById('sidebarPath').textContent = data.path;
    
    const tagsContainer = document.getElementById('sidebarTags');
    tagsContainer.innerHTML = data.tags.map(tag => 
        `<span class="tag">${tag}</span>`
    ).join('');

    // Submodules
    const submodulesContainer = document.getElementById('submodules');
    if (data.submodules) {
        submodulesContainer.innerHTML = `
            <h4><i class="fas fa-layer-group"></i> –ü–æ–¥—Ä–æ–±–Ω–µ–µ –æ –Ω–∞—Å—Ç—Ä–æ–π–∫–∞—Ö</h4>
            <div class="submodule-grid">
                ${data.submodules.map(sub => `
                    <div class="submodule-card">
                        <div class="submodule-header">
                            <div class="submodule-icon">
                                <i class="${sub.icon}"></i>
                            </div>
                            <h5>${sub.title}</h5>
                            <i class="fas fa-chevron-down submodule-toggle"></i>
                        </div>
                        <div class="submodule-content">
                            <ol class="submodule-steps">
                                ${sub.steps.map(step => `<li>${step}</li>`).join('')}
                            </ol>
                            <div class="submodule-info">
                                <p><strong><i class="fas fa-lightbulb"></i></strong> ${sub.tip}</p>
                                <p><strong><i class="fas fa-exclamation-triangle"></i></strong> ${sub.warning}</p>
                                <p><strong><i class="fas fa-map-signs"></i> –ü—É—Ç—å:</strong> ${sub.path}</p>
                            </div>
                        </div>
                    </div>
                `).join('')}
            </div>
        `;

        // Submodule toggles
        setTimeout(() => {
            document.querySelectorAll('.submodule-card').forEach(card => {
                card.addEventListener('click', () => {
                    card.classList.toggle('active');
                });
            });
        }, 100);
    } else {
        submodulesContainer.innerHTML = '';
    }

    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    modal.classList.remove('active');
    document.body.style.overflow = '';
}

modalClose.addEventListener('click', closeModal);
modal.addEventListener('click', (e) => {
    if (e.target === modal || e.target.classList.contains('modal-overlay')) {
        closeModal();
    }
});

document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape' && modal.classList.contains('active')) {
        closeModal();
    }
});

// Module cards
moduleCards.forEach(card => {
    const btn = card.querySelector('.module-btn');
    btn.addEventListener('click', () => {
        const moduleId = card.dataset.module;
        openModal(moduleId);
    });
});

// Prevent body scroll when modal/menu open
window.addEventListener('resize', () => {
    if (window.innerWidth >= 1024) {
        closeMobileMenu();
    }
});
// FAQ Module
document.addEventListener('DOMContentLoaded', function() {
    initFAQ();
});

function initFAQ() {
    const faqItems = document.querySelectorAll('.faq-item');
    const faqCategories = document.querySelectorAll('.faq-category');
    const faqSearch = document.getElementById('faqSearch');
    const faqShowAll = document.getElementById('faqShowAll');
    const faqNoResults = document.getElementById('faqNoResults');
    const faqGrid = document.getElementById('faqGrid');
    
    let allExpanded = false;
    
    // Toggle FAQ item
    faqItems.forEach(item => {
        const question = item.querySelector('.faq-question');
        
        question.addEventListener('click', (event) => {
            if (event.ctrlKey || event.metaKey) {
                toggleAllFAQItems(true);
                return;
            }
            
            item.classList.toggle('active');
            updateShowAllButton();
        });
    });
    
    // Category filter
    faqCategories.forEach(category => {
        category.addEventListener('click', () => {
            const categoryValue = category.dataset.category;
            
            faqCategories.forEach(c => c.classList.remove('active'));
            category.classList.add('active');
            
            filterFAQItems(categoryValue, faqSearch ? faqSearch.value.toLowerCase() : '');
        });
    });
    
    // Search
    if (faqSearch) {
        let searchTimeout;
        
        faqSearch.addEventListener('input', () => {
            clearTimeout(searchTimeout);
            searchTimeout = setTimeout(() => {
                const activeCategory = document.querySelector('.faq-category.active');
                const categoryValue = activeCategory ? activeCategory.dataset.category : 'all';
                filterFAQItems(categoryValue, faqSearch.value.toLowerCase());
            }, 300);
        });
        
        faqSearch.addEventListener('keydown', (e) => {
            if (e.key === 'Escape') {
                faqSearch.value = '';
                const activeCategory = document.querySelector('.faq-category.active');
                const categoryValue = activeCategory ? activeCategory.dataset.category : 'all';
                filterFAQItems(categoryValue, '');
            }
        });
    }
    
    // Show all / Hide all
    if (faqShowAll) {
        faqShowAll.addEventListener('click', () => {
            toggleAllFAQItems(!allExpanded);
        });
    }
    
    // Filter function
    function filterFAQItems(category, searchTerm = '') {
        let visibleCount = 0;
        
        faqItems.forEach(item => {
            const itemCategory = item.dataset.category;
            const itemText = item.innerText.toLowerCase();
            
            const categoryMatch = category === 'all' || itemCategory === category;
            const searchMatch = searchTerm === '' || itemText.includes(searchTerm);
            
            if (categoryMatch && searchMatch) {
                item.style.display = '';
                visibleCount++;
                
                item.style.animation = 'none';
                item.offsetHeight;
                item.style.animation = 'faqFadeIn 0.4s ease forwards';
            } else {
                item.style.display = 'none';
            }
        });
        
        if (faqNoResults) {
            if (visibleCount === 0) {
                faqNoResults.style.display = 'block';
                faqGrid.style.display = 'none';
            } else {
                faqNoResults.style.display = 'none';
                faqGrid.style.display = 'flex';
            }
        }
        
        updateShowAllButton();
    }
    
    // Toggle all FAQ items
    function toggleAllFAQItems(expand) {
        allExpanded = expand;
        
        faqItems.forEach(item => {
            if (item.style.display !== 'none') {
                if (expand) {
                    item.classList.add('active');
                } else {
                    item.classList.remove('active');
                }
            }
        });
        
        if (faqShowAll) {
            faqShowAll.innerHTML = expand ? 
                '<i class="fas fa-chevron-up"></i> –°–≤–µ—Ä–Ω—É—Ç—å –≤—Å–µ' : 
                '<i class="fas fa-chevron-down"></i> –†–∞–∑–≤–µ—Ä–Ω—É—Ç—å –≤—Å–µ';
        }
    }
    
    // Update show all button
    function updateShowAllButton() {
        if (!faqShowAll) return;
        
        const visibleItems = Array.from(faqItems).filter(item => item.style.display !== 'none');
        const activeItems = visibleItems.filter(item => item.classList.contains('active'));
        
        if (activeItems.length === visibleItems.length && visibleItems.length > 0) {
            allExpanded = true;
            faqShowAll.innerHTML = '<i class="fas fa-chevron-up"></i> –°–≤–µ—Ä–Ω—É—Ç—å –≤—Å–µ';
        } else {
            allExpanded = false;
            faqShowAll.innerHTML = '<i class="fas fa-chevron-down"></i> –†–∞–∑–≤–µ—Ä–Ω—É—Ç—å –≤—Å–µ';
        }
    }
    
    // Keyboard navigation
    document.addEventListener('keydown', (e) => {
        if (e.altKey && e.key === 'f' && faqSearch) {
            e.preventDefault();
            faqSearch.focus();
        }
        
        if (e.altKey && e.key === 'a') {
            e.preventDefault();
            const allCategory = document.querySelector('[data-category="all"]');
            if (allCategory) {
                allCategory.click();
            }
        }
    });
}